export function defaultPrivacyPolicy(ctx: {
  orgName: string;
  contactEmail?: string | null;
  contactAddress?: string | null;
}): string {
  return `# 개인정보처리방침

${ctx.orgName}(이하 "기관")는 「개인정보 보호법」을 비롯한 관련 법령을 준수하며, 다음과 같이 개인정보를 처리합니다.

## 1. 수집 항목
- 필수: 이름, 이메일 또는 휴대전화번호
- 선택: 생년월일, 주소
- 자동수집: 접속 로그, IP 주소, 쿠키

## 2. 수집 목적
- 후원 약정·결제·영수증 발급
- 본인 확인 및 계정 관리
- 후원 관련 알림(결제·영수증·후원 성과) 발송
- 법령에 따른 의무 준수

## 3. 보유 기간
- 회원 탈퇴 시까지 보유하되, 회계·세무 관련 기록은 관계 법령에 따라 최대 5년 보존합니다.

## 4. 제3자 제공
- 결제 처리를 위해 토스페이먼츠㈜에 결제정보가 제공됩니다.
- 국세청 연말정산 간소화 서비스에 기부금 영수증이 제출됩니다.

## 5. 정보주체의 권리
- 개인정보 열람·정정·삭제·처리정지를 요구할 수 있습니다.
- 마이페이지 설정에서 본인 데이터를 일괄 내보낼 수 있습니다.

## 6. 문의
${ctx.contactEmail ? `- 이메일: ${ctx.contactEmail}` : ""}
${ctx.contactAddress ? `- 주소: ${ctx.contactAddress}` : ""}

*본 방침은 기관 내부 사정에 따라 개정될 수 있으며, 변경 시 본 페이지에 고지됩니다.*
`;
}

export function defaultTerms(ctx: { orgName: string }): string {
  return `# 이용약관

## 제1조 (목적)
본 약관은 ${ctx.orgName}(이하 "기관")이 제공하는 후원 서비스(이하 "서비스")의 이용과 관련하여 기관과 이용자(이하 "회원")의 권리·의무 및 책임사항을 규정합니다.

## 제2조 (정의)
- "회원"이라 함은 기관의 후원 서비스에 가입·이용하는 개인 또는 단체를 말합니다.
- "정기후원"은 매월 자동 결제되는 후원을 말합니다.
- "일시후원"은 1회성 후원을 말합니다.

## 제3조 (약정의 체결)
회원은 서비스 내에서 후원 약정을 체결할 수 있으며, 약정은 회원이 후원하기 버튼을 누른 시점에 성립됩니다.

## 제4조 (결제 및 해지)
- 정기후원은 매월 지정일에 자동 결제됩니다.
- 회원은 언제든지 마이페이지에서 약정을 일시중지하거나 해지할 수 있습니다.
- 결제 실패 시 최대 3회 재시도 후 약정이 자동 일시중지됩니다.

## 제5조 (기부금 영수증)
- 기부금 영수증은 매년 1월 국세청 연말정산 간소화 서비스를 통해 제공됩니다.
- 회원은 마이페이지에서 본인의 영수증을 개별 다운로드할 수 있습니다.

## 제6조 (약관의 변경)
본 약관은 관계 법령 변경이나 서비스 개선을 위해 변경될 수 있으며, 변경 시 7일 전 공지합니다.

*본 약관은 공개된 시점부터 효력을 가집니다.*
`;
}
